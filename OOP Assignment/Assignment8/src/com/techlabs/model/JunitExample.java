package com.techlabs.model;

public class JunitExample {
	public int addNumbers(int num1, int num2) {
		return num1 + num2;
	}

	public int subNumbers(int a, int b) {
		return a - b;
	}

	public int multiplyNumbers(int a, int b) {
		return a * b;
	}

	public int divideNumbers(int a, int b) {
		return b / a;
	}
}
